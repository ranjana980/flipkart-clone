const express = require('express')
const router = express.Router()
const user = require('../controller/user')
const products = require('../controller/product')
router.post('/send_otp', user.sendOtp)
router.post('/verify_otp', user.verifyOtp)
router.get('/get_user', user.getUser)
router.get('/get_user/:id', user.getUserById)
router.post('/add_user', user.addUser)
router.post('/update_user/:id', user.updateUserById)
router.delete('/delete_user/:id', user.deleteUserById)
router.get('/get_products', products.getProducts)
router.post('/add_product', products.addProduct)
router.delete('/delete_product/:id', products.deleteProductById)
router.post('/update_product/:id', products.updateProductById)
router.post('/add_to_cart', products.addToCard)
router.post('/admin_auth', user.adminAuth)
module.exports = router